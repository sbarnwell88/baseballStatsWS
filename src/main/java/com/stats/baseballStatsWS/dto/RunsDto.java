package com.stats.baseballStatsWS.dto;

public class RunsDto {

    private int total;
    private int unearned;
    private int earned;
    private int ir;
    private int ira;
    private int bqr;
    private int bqra;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getUnearned() {
        return unearned;
    }

    public void setUnearned(int unearned) {
        this.unearned = unearned;
    }

    public int getEarned() {
        return earned;
    }

    public void setEarned(int earned) {
        this.earned = earned;
    }

    public int getIr() {
        return ir;
    }

    public void setIr(int ir) {
        this.ir = ir;
    }

    public int getIra() {
        return ira;
    }

    public void setIra(int ira) {
        this.ira = ira;
    }

    public int getBqr() {
        return bqr;
    }

    public void setBqr(int bqr) {
        this.bqr = bqr;
    }

    public int getBqra() {
        return bqra;
    }

    public void setBqra(int bqra) {
        this.bqra = bqra;
    }
}
